namespace RyuuseiManager.BinaryMagic
{
    public class FooterMagic
    {
        // Footer Magic
        // Next byte after the common footer header should be this:
        // 0x12: SF1
        // 0x14: SF2 N/S
        // 0x17: SF3
        // 0x18: SF2 Z
        // Importing save data from opposite version with exclusive giga card is considered illegal.
        public static readonly byte[] Eof = new byte[] { 0x5A, 0x5A, 0x94, 0x27, 0x07, 0x00, 0x00, 0x00, 0x04, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00 };

        public static readonly byte[] SwitchSaveFooterMagic = new byte[] { 0x47, 0x03, 0x90, 0x79, 0x02, 0x00, 0x00, 0x00, 0xB6, 0x95, 0x5D, 0x91, 0x55, 0x64, 0xFB, 0x02, 0x02, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00 };

        public class SF1
        {
            public static readonly byte[] MessageFooterMagic = new byte[] { 0x6D, 0x42, 0x15, 0xF6, 0x0F, 0x00, 0x00, 0x00 };
            public static readonly byte[] SecretFooterMagic = new byte[] { 0x24, 0xD6, 0xA5, 0x60, 0x09, 0x00, 0x00, 0x00 };
        }
    }
}
