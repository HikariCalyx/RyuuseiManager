namespace RyuuseiManager.BinaryMagic
{
    public class HeaderMagic
    {
        // Header Magic
        // The header magic here would check if the save data is encrypted from Steam, or from Switch, or raw.
        public static readonly byte[] Steam = { 0x44, 0x53, 0x53, 0x53, 0x02, 0x00, 0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00 };
        public static readonly byte[] Switch = { 0x44, 0x53, 0x53, 0x53, 0x02, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00 };
        // Next byte after the raw header should be this:
        // 0x43: SF1
        // 0x45: SF2
        // 0x53: SF3
        public static readonly byte[] Raw = { 0x99, 0xF1, 0xE3, 0xDB };

        public class SF1
        {
            public static readonly byte[] HeaderMagic = { 0x0F, 0x00, 0x00, 0x00 };
        }

        public class SF2
        {
            public static readonly byte[] HeaderMagic = { 0x0F, 0x00, 0x00, 0x00 };
        }

        public class SF3
        {
            public static readonly byte[] HeaderMagic = { 0x0F, 0x00, 0x00, 0x00 };
            public static readonly byte[] FolderHeaderMagic = { 0x53, 0xCF, 0xB5, 0x25, 0x4D, 0x13, 0xB8, 0x98, 0x0F, 0x00, 0x00, 0x00 };
            public static readonly byte[] RegCardHeaderMagic = { 0xE2, 0x7D, 0xB0, 0x71, 0x03, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00 };
            public static readonly byte[] WhiteCardHeaderMagic = { 0xAB, 0x2E, 0x29, 0x55, 0x04, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00 };
            public static readonly byte[] EquippedFolderHeaderMagic = { 0xBF, 0x5C, 0x87, 0xFD, 0x04, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00 };
        }
    }
}
