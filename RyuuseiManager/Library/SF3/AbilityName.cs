        public static string GetAbilityName(AbilityType abilityType, int language, int gameVersion)
        {
            Dictionary<AbilityType, string> targetLanguage = new Dictionary<AbilityType, string>();
            switch (language)
            {
                case 0: targetLanguage = en; break;
                case 1: targetLanguage = ja; break;
                case 2: targetLanguage = zh_cn; break;
                case 3: targetLanguage = zh_tw; break;
            }
            if (targetLanguage.TryGetValue(abilityType, out string? value))
            {
                if (abilityType == AbilityType.AcePrgm && gameVersion == 1)
                {
                    if (targetLanguage.TryGetValue(AbilityType.JokerPrgm, out string? value2))
                    {
                        return value2;
                    }
                    else
                    {
                        return "(null)";
                    }
                }
                return value;
            }
            else
            {
                return "(null)";
            }
        }
